class Solution:
    def intToRoman(self, num: int) -> str:
        values = [1000, 900, 500, 400, 100, 90, 50, 40, 10, 9, 5, 4, 1]
        symbols = [
            "M",
            "CM",
            "D",
            "CD",
            "C",
            "XC",
            "L",
            "XL",
            "X",
            "IX",
            "V",
            "IV",
            "I",
        ]

        value_to_symbol = dict(zip(values, symbols))

        result = ""

        for value in values:
            while num >= value:
                num -= value
                result += value_to_symbol[value]

        return result
